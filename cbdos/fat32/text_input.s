	.code

;-----------------------------------------------------------------------------
; to_upper
;-----------------------------------------------------------------------------
to_upper:
	; Lower case character?
	cmp #'a'
	bcc @done
	cmp #'z'+1
	bcs @done

	; Make uppercase
	and #$DF
@done:	rts
